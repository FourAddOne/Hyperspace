CREATE TABLE `message` (
  `id` bigint unsigned NOT NULL AUTO_INCREMENT COMMENT '数据库内部自增主键',
  `message_id` varchar(64) NOT NULL COMMENT '消息全局唯一ID（业务主键，如：msg_${UUID}）',
  `type` varchar(20) NOT NULL COMMENT '消息类型（text-文本、image-图片、quote-引用、system-系统通知等）',
  `from_user_id` varchar(64) NOT NULL COMMENT '发送者用户ID（关联user.user_id）',
  `from_username` varchar(100) DEFAULT '' COMMENT '发送者用户名（冗余存储，优化展示性能）',
  `to_target_id` varchar(64) NOT NULL COMMENT '接收目标ID：群聊=群组ID，单聊=对方用户ID',
  `to_target_type` varchar(20) NOT NULL COMMENT '接收目标类型（group-群聊、user-单聊）',
  `to_target_name` varchar(100) DEFAULT '' COMMENT '接收目标名称（冗余存储：群聊=群名，单聊=对方用户名）',
  `text_content` text COMMENT '文本消息内容（type为text/quote时必填）',
  `image_urls` text COMMENT '图片URL集合（JSON格式，如：["https://xxx/1.jpg"]，type=image时必填）',
  `client_timestamp` bigint NOT NULL COMMENT '客户端发送时间戳（毫秒级，保障时序性）',
  `server_timestamp` bigint NOT NULL COMMENT '服务器接收时间戳（毫秒级，存储排序基准）',
  `status` varchar(20) NOT NULL COMMENT '消息状态（sending-发送中、success-成功、failed-失败、recall-已撤回）',
  `quote_message_id` varchar(64) DEFAULT '' COMMENT '引用消息ID（关联本表message_id，无引用则为空）',
  `device_type` varchar(50) DEFAULT '' COMMENT '发送端设备类型（ios、android、web、windows等）',
  `device_name` varchar(100) DEFAULT '' COMMENT '发送端设备名称（如：iPhone 15、Chrome浏览器）',
  `create_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间（自动生成）',
  `update_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '记录更新时间（自动更新）',
  `rich_content` text COMMENT '富文本内容（JSON格式，存储带格式文本、表情、链接卡片等）',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_message_message_id` (`message_id`),
  KEY `idx_message_client_timestamp` (`client_timestamp`),
  KEY `idx_message_from_user_id` (`from_user_id`),
  KEY `idx_message_server_timestamp` (`server_timestamp`),
  KEY `idx_message_status` (`status`),
  KEY `idx_message_target_type_server_time` (`to_target_type`,`server_timestamp`),
  KEY `idx_message_to_target_id` (`to_target_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='统一消息存储表';